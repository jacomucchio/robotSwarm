package it.unicam.cs.pa.robotSwarm.model;

/**
 * An interface representing a label.
 */
public interface ILabel {
    /**
     * Gets the identifier of the label.
     *
     * @return The identifier of the label.
     */
    String getId();
    /**
     * Sets the identifier of the label.
     *
     * @param id The identifier to be set for the label.
     */
    void setId(String id);
}
